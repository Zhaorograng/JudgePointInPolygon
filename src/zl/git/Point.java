package zl.git;


/**
 * 坐标类，包含经纬度信息
 * 
 * @author zl
 * 
 */
public class Point {
	private double x;
	private double y;
	private String info;

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}
}
